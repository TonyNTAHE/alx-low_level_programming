#include "3-calc.h"

/**
 * op_add - add two number
 * @a: first number.
 * @b: second number.
 * Return: sum of two numbers.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substrate two numbers.
 * @a: first number
 * @b: second number.
 * Return: substration of two numbers.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the product of tow numbers.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers.
 * @a: first number.
 * @b: second number.
 * Return: division of two numbers.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the mod of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: modular of two numbers.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
